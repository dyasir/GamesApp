package com.unity3d.player.model;

public class FusionBean {

    private int video_show_number;
    private int watch_video_longtime;
    private int app_start_number;
    private boolean app_change_enable;

    public int getVideo_show_number() {
        return video_show_number;
    }

    public void setVideo_show_number(int video_show_number) {
        this.video_show_number = video_show_number;
    }

    public int getWatch_video_longtime() {
        return watch_video_longtime;
    }

    public void setWatch_video_longtime(int watch_video_longtime) {
        this.watch_video_longtime = watch_video_longtime;
    }

    public int getApp_start_number() {
        return app_start_number;
    }

    public void setApp_start_number(int app_start_number) {
        this.app_start_number = app_start_number;
    }

    public boolean isApp_change_enable() {
        return app_change_enable;
    }

    public void setApp_change_enable(boolean app_change_enable) {
        this.app_change_enable = app_change_enable;
    }
}
